def generate_answer(conversation):
    response = client.chat.completions.create(
    model=st.secrets["deployment_id_gpt4"],
    messages=conversation,
    temperature=0,
    max_tokens=1000,
    top_p=1,
    frequency_penalty=0,
    presence_penalty=0,
    stop = [' END']
    )
    return (response.choices[0].message.content).strip()

